# creating a list 
fruits = ["apple","banana","dates","guava"]
print(fruits[2])

# changing the elements in a list 
fruits[1] = "mangoes"
print(fruits)

# adding some elemets in the list 
fruits.append("grapes")
print(fruits)

# adding many items in a list at a time together 
fruits.extend(["peaches","pineapple"])
print(fruits)